public class Expression {
    public static void main(String[] args) {
        
        // Expression = operands and operators
        // Operands = values, variables, numbers, quantity
        // Operators = + - * / %

        double friends = 10;
        friends = friends + 2;  // Here the plus(+) sign is operator and 2 is operand
        friends = (double) friends / 5; // If we want to change our integer data type to double, then we need to write 'double' inside parentheses.

        System.out.println(friends);
    }
}
